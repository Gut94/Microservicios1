package com.microserv1.proyMicroServicios1.datos;

public class Parametros {

  public Parametros() {

    super();
  }

  String nombre;

  String colorBuscado;

  int numeroCoches;

  int numeroMotos;

  public String getNombre() {

    return this.nombre;
  }

  public void setNombre(String nombre) {

    this.nombre = nombre;
  }

  public String getColorBuscado() {

    return this.colorBuscado;
  }

  public void setColorBuscado(String colorBuscado) {

    this.colorBuscado = colorBuscado;
  }

  public int getNumeroCoches() {

    return this.numeroCoches;
  }

  public void setNumeroCoches(int numeroCoches) {

    this.numeroCoches = numeroCoches;
  }

  public int getNumeroMotos() {

    return this.numeroMotos;
  }

  public void setNumeroMotos(int numeroMotos) {

    this.numeroMotos = numeroMotos;
  }

}
